import { NextRequest, NextResponse } from 'next/server';

import { PERMISSIONS } from '@/core/rbac/permission';
import { getSignUser } from '@/shared/models/user';
import { addShowcase, getShowcases } from '@/shared/models/showcase';
import { getStorageService } from '@/shared/services/storage';

export async function GET(req: NextRequest) {
  const searchParams = req.nextUrl.searchParams;
  const keyword = searchParams.get('keyword') || undefined;
  const page = Number(searchParams.get('page')) || 1;
  const limit = Number(searchParams.get('limit')) || 20;

  // Check if user is admin to decide which function to call
  const user = await getSignUser();
  let isAdmin = false;
  if (user) {
    // We can't easily check permission here without importing from rbac service which is async
    // But we can check if the user has the permission
    // For now, let's assume if they have the permission they want the admin view?
    // Or we can add a query param 'mode=admin' and check permission.
    // Let's try to check permission.
    try {
       // We need to import hasPermission from rbac service
       const { hasPermission } = await import('@/shared/services/rbac');
       isAdmin = await hasPermission(user.id, PERMISSIONS.SHOWCASES_READ);
    } catch (e) {
      isAdmin = false;
    }
  }

  let result;
  if (isAdmin) {
     const { getShowcasesAdmin } = await import('@/shared/models/showcase');
     result = await getShowcasesAdmin({ keyword, page, limit });
  } else {
     result = await getShowcases({ keyword, page, limit });
  }

  return NextResponse.json(result);
}

